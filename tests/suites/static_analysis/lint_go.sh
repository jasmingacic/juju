run_ineffassign() {
  FOLDERS="${2}"
  # shellcheck disable=SC2086
  OUT=$(ineffassign ${FOLDERS} | grep "github.com/juju/juju" | sed -E "s/^(.+src\\/github\\.com\\/juju\\/juju\\/)(.+)/\2/")
  if [ -n "${OUT}" ]; then
