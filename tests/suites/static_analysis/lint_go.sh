run_go_fmt() {
  FILES=${2}
  OUT=$(echo "${FILES}" | xargs gofmt -l -s)
  if [ -n "${OUT}" ]; then
    OUT=$(echo "${OUT}" | sed "s/^/  /")
    echo ""
    echo "$(red 'Found some issues (gofmt):')"
    for ITEM in ${OUT}; do
      echo -e "\\ngofmt -s -w ${ITEM}" >&2
    done
    exit 1
  fi
}

run_go_tidy() {
  CUR_SHA=$(git show HEAD:go.sum | shasum -a 1 | awk '{ print $1 }')
  go mod tidy 2>&1
  NEW_SHA=$(cat go.sum | shasum -a 1 | awk '{ print $1 }')
  if [ "${CUR_SHA}" != "${NEW_SHA}" ]; then
      (>&2 echo -e "\\nError: go mod sum is out of sync. Run 'go mod tidy' and commit source.")
      exit 1
  fi
}

